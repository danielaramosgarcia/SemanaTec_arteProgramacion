"""Draw square using path at (x, y)."""
def square(x, y):
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

"""Return offset of point in tiles."""
def offset(point):
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

"""Return True if point is valid in tiles."""
def valid(point):
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

"""Draw world using path."""
def world():
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

"""Calcular direccion hacia pacman para hacer inteligentes a los fantasmas"""
def moverHaciaPacman(posicionP, posicionG):
    distanciaX =   posicionP.x - posicionG.x
    distanciaY = posicionP.y - posicionG.y
    
    if abs(distanciaX) > abs(distanciaY):
        return vector(distanciaX / abs(distanciaX), 0)
    else:
        return vector(0, distanciaY / abs(distanciaY))
    
"""Move pacman and all ghosts."""
def move():
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')
    
    for ghost in ghosts:
        dirHaciaPacman = moverHaciaPacman(pacman, ghost[0])
        ghost[1] = dirHaciaPacman
        
    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            options = [
                vector(20, 0),
                vector(-20, 0),
                vector(0, 20),
                vector(0, -20),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

